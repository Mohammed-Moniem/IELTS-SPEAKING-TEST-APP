const ENTRIES_PER_GROUP = 250;
const numGroups = Math.ceil(allEntries.length / ENTRIES_PER_GROUP);
const startGroup = 34; // group34 onwards

for (let g = 0; g < numGroups; g++) {
  const groupNum = startGroup + g;
  const slice = allEntries.slice(g * ENTRIES_PER_GROUP, (g + 1) * ENTRIES_PER_GROUP);

  // Group by topic
  const byTopic = {};
  for (const { topic, entry } of slice) {
    if (!byTopic[topic]) byTopic[topic] = [];
    byTopic[topic].push(entry);
  }

  // Build file content
  let content = `// Auto-generated vocab boost group ${groupNum}\nmodule.exports = {\n  vocab: {\n`;
  for (const [topic, entries] of Object.entries(byTopic)) {
    content += `    ${topic}: [\n`;
    for (const [lemma, pos, def, syn, cefr] of entries) {
      const safeDef = def.replace(/'/g, "\\'");
      const safeSyn = syn.replace(/'/g, "\\'");
      content += `      ['${lemma}','${pos}','${safeDef}','${safeSyn}','${cefr}'],\n`;
    }
    content += `    ],\n`;
  }
  content += `  },\n  collocations: {}\n};\n`;

  const filePath = path.join(OUTPUT_DIR, `group${groupNum}.js`);
  fs.writeFileSync(filePath, content);
  console.log(`  group${groupNum}.js: ${slice.length} vocab entries`);
}

