/**
 * generate-vocab-boost.js
 * Massively expands vocabulary by using Academic Word List families +
 * aggressive morphological derivation across all 25 IELTS topics.
 * Outputs group34-group80 (vocab only) to supplement existing groups.
 */

const fs = require('fs');
const path = require('path');
const OUTPUT_DIR = path.join(__dirname, 'library-data');

// ── Load ALL existing words (groups 1-33) ─────────────────────────
const existingVocab = new Set();
for (let i = 1; i <= 33; i++) {
  try {
    const mod = require(path.join(OUTPUT_DIR, `group${i}.js`));
    if (mod.vocab) for (const entries of Object.values(mod.vocab)) for (const e of entries) existingVocab.add(e[0].toLowerCase());
  } catch (_) {}
}
console.log(`Loaded existing vocab: ${existingVocab.size} entries`);

const allNewVocab = new Set();
const vocabByTopic = {};
const TOPICS = [
  'education','technology','health','environment','business',
  'economy','society','culture','science','crime',
  'media','communication','travel','government','psychology',
  'arts','sports','food','housing','transport',
  'employment','family','history','language','globalization'
];
for (const t of TOPICS) vocabByTopic[t] = [];

// ── ENHANCED DERIVATION ENGINE ────────────────────────────────────
function tryAdd(lemma, pos, def, syn, cefr, topic) {
  const key = lemma.toLowerCase();
  if (existingVocab.has(key) || allNewVocab.has(key)) return false;
  if (!/^[a-z][a-z '-]{1,35}$/.test(key)) return false;
  allNewVocab.add(key);
  vocabByTopic[topic].push([lemma, pos, def, syn, cefr]);
  return true;
}

const CEFR_UP = { A2:'B1', B1:'B2', B2:'C1', C1:'C2', C2:'C2' };

function deriveFamily(lemma, pos, baseDef, baseSyn, cefr, topic) {
  const s1 = baseSyn.split(',')[0].trim();

  if (pos === 'v') {
    const stem = lemma.replace(/e$/, '');
    // Nominalisations
    tryAdd(stem + 'ation', 'n', `The act of ${lemma.replace(/e$/,'')}ing`, s1+' process', cefr, topic);
    tryAdd(stem + 'ment', 'n', `The result of ${lemma.replace(/e$/,'')}ing`, s1+' outcome', cefr, topic);
    tryAdd(stem + 'ance', 'n', `The state of ${lemma.replace(/e$/,'')}ing`, s1+' state', cefr, topic);
    tryAdd(stem + 'ence', 'n', `The quality of ${lemma.replace(/e$/,'')}ing`, s1+' quality', cefr, topic);
    tryAdd(stem + 'al', 'n', `An act of ${lemma.replace(/e$/,'')}ing`, s1+' event', CEFR_UP[cefr], topic);
    // Agent nouns
    tryAdd(stem + 'er', 'n', `One who ${lemma}s`, s1+' person', cefr, topic);
    tryAdd(stem + 'or', 'n', `One who ${lemma}s`, s1+' agent', cefr, topic);
    tryAdd(stem + 'ee', 'n', `One who is ${lemma}d`, s1+' recipient', CEFR_UP[cefr], topic);
    // Adjective forms
    tryAdd(stem + 'able', 'adj', `Capable of being ${lemma}d`, s1+',possible', CEFR_UP[cefr], topic);
    tryAdd(stem + 'ible', 'adj', `Able to be ${lemma}d`, s1+',feasible', CEFR_UP[cefr], topic);
    tryAdd(stem + 'ive', 'adj', `Tending to ${lemma}`, s1+',active', cefr, topic);
    tryAdd(stem + 'ing', 'adj', `Currently ${lemma.replace(/e$/,'')}ing`, s1+',ongoing', cefr, topic);
    tryAdd(stem + 'ed', 'adj', `Having been ${lemma}d`, s1+',completed', cefr, topic);
    // Gerund
    tryAdd(stem + 'ing', 'n', `The process of ${lemma.replace(/e$/,'')}ing`, s1+' activity', cefr, topic);
    // Prefix variations
    tryAdd('re' + lemma, 'v', `To ${lemma} again`, s1+' again,repeat', cefr, topic);
    tryAdd('un' + lemma, 'v', `To reverse ${lemma.replace(/e$/,'')}ing`, 'reverse,undo', CEFR_UP[cefr], topic);
    tryAdd('over' + lemma, 'v', `To ${lemma} excessively`, 'excess,too much', CEFR_UP[cefr], topic);
    tryAdd('under' + lemma, 'v', `To ${lemma} insufficiently`, 'insufficient,inadequate', CEFR_UP[cefr], topic);
    tryAdd('mis' + lemma, 'v', `To ${lemma} incorrectly`, 'wrongly,incorrectly', CEFR_UP[cefr], topic);
    tryAdd('pre' + lemma, 'v', `To ${lemma} beforehand`, 'before,in advance', CEFR_UP[cefr], topic);
    tryAdd('co' + lemma, 'v', `To ${lemma} together`, 'jointly,together', CEFR_UP[cefr], topic);
    // Double derivations: re+verb → re+verb+ation
    tryAdd('re' + stem + 'ation', 'n', `The act of ${lemma.replace(/e$/,'')}ing again`, 're-'+s1, CEFR_UP[cefr], topic);
    tryAdd('over' + stem + 'ation', 'n', `Excessive ${lemma.replace(/e$/,'')}ing`, 'excess '+s1, CEFR_UP[cefr], topic);
  }

  if (pos === 'n') {
    tryAdd(lemma + 'al', 'adj', `Relating to ${lemma}`, s1+'-related,pertaining to', cefr, topic);
    tryAdd(lemma + 'ic', 'adj', `Of or relating to ${lemma}`, s1+'-based,characteristic', cefr, topic);
    tryAdd(lemma + 'ous', 'adj', `Full of ${lemma}`, s1+'-filled,abundant', CEFR_UP[cefr], topic);
    tryAdd(lemma + 'ful', 'adj', `Full of ${lemma}`, s1+'-rich,abounding', cefr, topic);
    tryAdd(lemma + 'less', 'adj', `Without ${lemma}`, 'lacking,without', cefr, topic);
    tryAdd(lemma + 'ism', 'n', `The ideology of ${lemma}`, s1+' ideology,doctrine', CEFR_UP[cefr], topic);
    tryAdd(lemma + 'ist', 'n', `A person focused on ${lemma}`, s1+' specialist,advocate', CEFR_UP[cefr], topic);
    tryAdd(lemma + 'ise', 'v', `To make into ${lemma}`, s1+',convert', CEFR_UP[cefr], topic);
    tryAdd(lemma + 'ize', 'v', `To make or become ${lemma}`, s1+',transform', CEFR_UP[cefr], topic);
    tryAdd('anti' + lemma, 'adj', `Opposed to ${lemma}`, 'against,opposing', CEFR_UP[cefr], topic);
    tryAdd('non' + lemma, 'adj', `Not involving ${lemma}`, 'without,lacking', cefr, topic);
    tryAdd('pre' + lemma, 'n', `Before ${lemma}`, 'preceding,prior', CEFR_UP[cefr], topic);
    tryAdd('post' + lemma, 'n', `After ${lemma}`, 'following,subsequent', CEFR_UP[cefr], topic);
    tryAdd('sub' + lemma, 'n', `A subdivision of ${lemma}`, 'subset,branch', CEFR_UP[cefr], topic);
    tryAdd('multi' + lemma, 'adj', `Involving many ${lemma}s`, 'multiple,several', CEFR_UP[cefr], topic);
    tryAdd('inter' + lemma, 'adj', `Between ${lemma}s`, 'cross,between', CEFR_UP[cefr], topic);
  }

  if (pos === 'adj') {
    const stem2 = lemma.endsWith('y') ? lemma.slice(0,-1) : lemma;
    tryAdd(stem2 + (lemma.endsWith('y') ? 'ily' : 'ly'), 'adv', `In a ${lemma} manner`, s1+' way', cefr, topic);
    tryAdd(stem2 + 'ness', 'n', `The quality of being ${lemma}`, s1+' quality', cefr, topic);
    tryAdd(stem2 + 'ity', 'n', `The state of being ${lemma}`, s1+' condition', CEFR_UP[cefr], topic);
    tryAdd('un' + lemma, 'adj', `Not ${lemma}`, 'not '+s1, cefr, topic);
    tryAdd('in' + lemma, 'adj', `Not ${lemma}`, 'not '+s1, CEFR_UP[cefr], topic);
    tryAdd('non' + lemma, 'adj', `Not ${lemma}`, 'not '+s1, cefr, topic);
    tryAdd('semi' + lemma, 'adj', `Partially ${lemma}`, 'partly '+s1, CEFR_UP[cefr], topic);
    tryAdd('over' + lemma, 'adj', `Excessively ${lemma}`, 'too '+s1, CEFR_UP[cefr], topic);
    tryAdd('ultra' + lemma, 'adj', `Extremely ${lemma}`, 'very '+s1, CEFR_UP[cefr], topic);
  }
}

