// GENERATION LOGIC
// ============================================================
const TOPICS = Object.keys(SEED_VOCAB);
const vocabByTopic = {};
const collByTopic = {};

for (const topic of TOPICS) {
  vocabByTopic[topic] = [];
  collByTopic[topic] = [];

  // 1. Add seed vocab (skip existing)
  for (const entry of SEED_VOCAB[topic] || []) {
    const key = entry[0].toLowerCase();
    if (!existingVocab.has(key) && !allVocab.has(key)) {
      vocabByTopic[topic].push(entry);
      allVocab.add(key);
    }
  }

  // 2. Derive additional words from seed vocab
  for (const entry of SEED_VOCAB[topic] || []) {
    const derived = deriveWords(entry[0], entry[1], entry[3], entry[3], entry[4]);
    for (const d of derived) {
      const key = d[0].toLowerCase();
      if (!allVocab.has(key) && /^[a-z-]+$/.test(key) && key.length > 2 && key.length < 25) {
        vocabByTopic[topic].push(d);
        allVocab.add(key);
      }
    }
  }

  // 3. Add seed collocations (skip existing)
  for (const entry of SEED_COLLOCATIONS[topic] || []) {
    const key = entry[0].toLowerCase();
    if (!existingCollocations.has(key) && !allCollocations.has(key)) {
      collByTopic[topic].push(entry);
      allCollocations.add(key);
    }
  }

  // 4. Generate additional collocations from seed vocab nouns
  const nouns = (SEED_VOCAB[topic] || []).filter(e => e[1] === 'n').map(e => e[0]);
  const verbs = (SEED_VOCAB[topic] || []).filter(e => e[1] === 'v').map(e => e[0]);
  const adjs = (SEED_VOCAB[topic] || []).filter(e => e[1] === 'adj').map(e => e[0]);

  // verb + noun collocations
  const collVerbs = ['improve','develop','enhance','promote','address','tackle','maintain','achieve','establish','consider','evaluate','analyse','implement','support','reduce','increase','manage','provide','ensure','monitor'];
  const collAdjs = ['significant','essential','effective','comprehensive','innovative','sustainable','fundamental','crucial','ongoing','potential','substantial','remarkable','profound','widespread','unprecedented'];

  for (const noun of nouns) {
    for (const cv of collVerbs) {
      const phrase = `${cv} ${noun}`;
      const key = phrase.toLowerCase();
      if (!existingCollocations.has(key) && !allCollocations.has(key)) {
        const cefr = noun.length > 10 ? 'C1' : noun.length > 7 ? 'B2' : 'B1';
        collByTopic[topic].push([phrase, `To ${cv} ${noun} in the context of ${topic}`, `${collVerbs[(collVerbs.indexOf(cv)+3)%collVerbs.length]} ${noun}`, cefr]);
        allCollocations.add(key);
      }
    }
    for (const ca of collAdjs.slice(0, 8)) {
      const phrase = `${ca} ${noun}`;
      const key = phrase.toLowerCase();
      if (!existingCollocations.has(key) && !allCollocations.has(key)) {
        const cefr = ca.length > 10 ? 'B2' : 'B1';
        collByTopic[topic].push([phrase, `A ${ca} type of ${noun}`, `${collAdjs[(collAdjs.indexOf(ca)+2)%collAdjs.length]} ${noun}`, cefr]);
        allCollocations.add(key);
      }
    }
  }

  // adj + noun collocations
  for (const adj of adjs) {
    for (const noun of nouns.slice(0, 5)) {
      const phrase = `${adj} ${noun}`;
      const key = phrase.toLowerCase();
      if (!existingCollocations.has(key) && !allCollocations.has(key)) {
        collByTopic[topic].push([phrase, `${noun} that is ${adj}`, `${adj} ${nouns[(nouns.indexOf(noun)+1)%nouns.length]}`, 'B2']);
        allCollocations.add(key);
      }
    }
  }
}

