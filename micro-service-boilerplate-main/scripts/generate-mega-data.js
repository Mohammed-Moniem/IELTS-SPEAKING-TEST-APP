// WRITE GROUP FILES
// ============================================================
function formatGroupFile(vocabEntries, collEntries) {
  const vocabByTopicLocal = {};
  const collByTopicLocal = {};
  for (const e of vocabEntries) {
    if (!vocabByTopicLocal[e.topic]) vocabByTopicLocal[e.topic] = [];
    vocabByTopicLocal[e.topic].push(`    ['${e.entry[0].replace(/'/g,"\\'")}','${e.entry[1]}','${e.entry[2].replace(/'/g,"\\'")}','${e.entry[3].replace(/'/g,"\\'")}','${e.entry[4]}']`);
  }
  for (const e of collEntries) {
    if (!collByTopicLocal[e.topic]) collByTopicLocal[e.topic] = [];
    collByTopicLocal[e.topic].push(`    ['${e.entry[0].replace(/'/g,"\\'")}','${e.entry[1].replace(/'/g,"\\'")}','${e.entry[2].replace(/'/g,"\\'")}','${e.entry[3]}']`);
  }

  let content = 'module.exports = {\n  vocab: {\n';
  for (const [topic, lines] of Object.entries(vocabByTopicLocal)) {
    content += `    ${topic}: [\n${lines.join(',\n')}\n    ],\n`;
  }
  content += '  },\n  collocations: {\n';
  for (const [topic, lines] of Object.entries(collByTopicLocal)) {
    content += `    ${topic}: [\n${lines.join(',\n')}\n    ],\n`;
  }
  content += '  }\n};\n';
  return content;
}

