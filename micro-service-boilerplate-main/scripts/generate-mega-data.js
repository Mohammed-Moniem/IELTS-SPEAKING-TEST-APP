const PREFIXES = ['un', 're', 'over', 'under', 'mis', 'pre', 'post', 'anti', 'co', 'multi', 'non'];

function deriveWords(lemma, pos, baseDef, baseSyn, cefr) {
  const results = [];
  const cefrUp = { A2: 'B1', B1: 'B2', B2: 'C1', C1: 'C2', C2: 'C2' };

  if (pos === 'v') {
    // verb → noun (e.g., educate → education)
    const nounForm = lemma.replace(/e$/, '') + 'ation';
    if (nounForm !== lemma && /^[a-z]+$/.test(nounForm))
      results.push([nounForm, 'n', `The act or process of ${lemma.replace(/e$/, '')}ing`, baseSyn.split(',').map(s => s.trim() + ' process').join(','), cefr]);
    // verb → agent noun
    const agentForm = lemma.replace(/e$/, '') + 'er';
    if (/^[a-z]+$/.test(agentForm))
      results.push([agentForm, 'n', `A person who ${lemma}s`, baseSyn.split(',').map(s => s.trim() + ' person').join(','), cefrUp[cefr]]);
    // verb → adjective -able
    const ableForm = lemma.replace(/e$/, '') + 'able';
    if (/^[a-z]+$/.test(ableForm))
      results.push([ableForm, 'adj', `Able to be ${lemma}d`, baseSyn.split(',')[0] + ',possible', cefrUp[cefr]]);
    // verb → gerund noun
    const ingForm = lemma.replace(/e$/, '') + 'ing';
    results.push([ingForm, 'n', `The process of ${lemma.replace(/e$/, '')}ing something`, baseSyn.split(',')[0] + ' activity', cefr]);
  }
  if (pos === 'n') {
    // noun → adjective -al
    const alForm = lemma + 'al';
    if (/^[a-z]+$/.test(alForm))
      results.push([alForm, 'adj', `Relating to ${lemma}`, baseSyn.split(',')[0] + '-related', cefr]);
  }
  if (pos === 'adj') {
    // adj → adverb -ly
    const lyForm = lemma.endsWith('y') ? lemma.slice(0, -1) + 'ily' : lemma + 'ly';
    results.push([lyForm, 'adv', `In a ${lemma} manner`, baseSyn.split(',')[0] + ' way', cefr]);
    // adj → noun -ness
    const nessForm = lemma + 'ness';
    results.push([nessForm, 'n', `The quality of being ${lemma}`, baseSyn.split(',')[0] + ' quality', cefrUp[cefr]]);
    // un- prefix
    const unForm = 'un' + lemma;
    results.push([unForm, 'adj', `Not ${lemma}`, 'not ' + baseSyn.split(',')[0], cefr]);
  }
  return results.filter(r => !existingVocab.has(r[0].toLowerCase()) && !allVocab.has(r[0].toLowerCase()));
}

// ============================================================
// COLLOCATION PATTERN ENGINE
// ============================================================
const VERB_PATTERNS = ['make', 'take', 'give', 'have', 'do', 'get', 'set', 'run', 'keep', 'put', 'bring', 'hold', 'carry', 'reach', 'raise', 'build', 'create', 'develop', 'achieve', 'gain'];
const ADJ_PATTERNS = ['strong', 'great', 'major', 'key', 'significant', 'serious', 'heavy', 'deep', 'wide', 'high', 'growing', 'increasing', 'important', 'critical', 'vital', 'essential', 'primary', 'main', 'basic', 'fundamental'];

function generateCollocations(topicNouns, topicVerbs, topic, cefr) {
  const results = [];
  for (const noun of topicNouns) {
    for (const verb of VERB_PATTERNS.slice(0, 5)) {
      const phrase = `${verb} ${noun}`;
      if (!existingCollocations.has(phrase.toLowerCase()) && !allCollocations.has(phrase.toLowerCase())) {
        results.push([phrase, `To ${verb} ${noun} in the context of ${topic}`, `${VERB_PATTERNS[Math.floor(Math.random()*VERB_PATTERNS.length)]} ${noun}`, cefr]);
      }
    }
    for (const adj of ADJ_PATTERNS.slice(0, 3)) {
      const phrase = `${adj} ${noun}`;
      if (!existingCollocations.has(phrase.toLowerCase()) && !allCollocations.has(phrase.toLowerCase())) {
        results.push([phrase, `A ${adj} type of ${noun}`, `${ADJ_PATTERNS[Math.floor(Math.random()*ADJ_PATTERNS.length)]} ${noun}`, cefr]);
      }
    }
  }
  return results;
}

