function buildGroup7() {
  const vocab = G7_VOCAB;
  const collocations = G7_COLLOCATIONS;
  for (const [topic, entries] of Object.entries(vocab)) {
    for (let i = 0; i < entries.length; i++) {
      if (entries[i].length === 4) entries[i].push(pickCefr(i));
    }
  }
  for (const [topic, entries] of Object.entries(collocations)) {
    for (let i = 0; i < entries.length; i++) {
      if (entries[i].length === 3) entries[i].push(pickCefr(i));
    }
  }
  return { vocab, collocations };
}

function writeGroupFile(filename, data) {
  const outPath = path.join(__dirname, 'library-data', filename);
  let js = `'use strict';\nmodule.exports = {\n  vocab: {\n`;
  for (const [topic, entries] of Object.entries(data.vocab)) {
    js += `    ${topic}: [\n`;
    for (const e of entries) js += `      ${JSON.stringify(e)},\n`;
    js += `    ],\n`;
  }
  js += `  },\n  collocations: {\n`;
  for (const [topic, entries] of Object.entries(data.collocations)) {
    js += `    ${topic}: [\n`;
    for (const e of entries) js += `      ${JSON.stringify(e)},\n`;
    js += `    ],\n`;
  }
  js += `  },\n};\n`;
  fs.writeFileSync(outPath, js);
  let vc = 0, cc = 0;
  for (const e of Object.values(data.vocab)) vc += e.length;
  for (const e of Object.values(data.collocations)) cc += e.length;
  console.log(`✅ ${filename}: ${vc} vocab + ${cc} collocations = ${vc + cc} total`);
  return { vc, cc };
}

