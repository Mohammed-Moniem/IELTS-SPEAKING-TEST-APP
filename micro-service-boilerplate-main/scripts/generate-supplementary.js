// Add CEFR levels to group6
function buildGroup6() {
  const vocab = G6_VOCAB;
  const collocations = G6_COLLOCATIONS;

  for (const [topic, entries] of Object.entries(vocab)) {
    for (let i = 0; i < entries.length; i++) {
      if (entries[i].length === 4) {
        entries[i].push(pickCefr(i));
      }
    }
  }

  for (const [topic, entries] of Object.entries(collocations)) {
    for (let i = 0; i < entries.length; i++) {
      if (entries[i].length === 3) {
        entries[i].push(pickCefr(i));
      }
    }
  }

  return { vocab, collocations };
}

