function writeGroupFile(filename, data) {
  const outPath = path.join(__dirname, 'library-data', filename);

  let js = `'use strict';\nmodule.exports = {\n  vocab: {\n`;

  for (const [topic, entries] of Object.entries(data.vocab)) {
    js += `    ${topic}: [\n`;
    for (const e of entries) {
      js += `      ${JSON.stringify(e)},\n`;
    }
    js += `    ],\n`;
  }
  js += `  },\n  collocations: {\n`;

  for (const [topic, entries] of Object.entries(data.collocations)) {
    js += `    ${topic}: [\n`;
    for (const e of entries) {
      js += `      ${JSON.stringify(e)},\n`;
    }
    js += `    ],\n`;
  }
  js += `  },\n};\n`;

  fs.writeFileSync(outPath, js);

  // Count entries
  let vocabCount = 0, collocCount = 0;
  for (const entries of Object.values(data.vocab)) vocabCount += entries.length;
  for (const entries of Object.values(data.collocations)) collocCount += entries.length;

  console.log(`✅ ${filename}: ${vocabCount} vocab + ${collocCount} collocations = ${vocabCount + collocCount} total`);
  return { vocabCount, collocCount };
}

